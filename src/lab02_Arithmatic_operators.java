public class lab02_Arithmatic_operators {
    public static void main(String[] args) {
        int a = 10;
        int b = 2;
        float c = 2f;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b); // 6
        System.out.println(a/c); // 6.6666665


    }
}
